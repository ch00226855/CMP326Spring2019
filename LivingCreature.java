
public interface LivingCreature {
	
	/**
	 * This methods should be implemented so that the living creature
	 * can say something.
	 */
	public void speak();
	
	/**
	 * This methods will allow the living creature same something 
	 * multiple times
	 * @param num The number of times something was said.
	 */
	public void speak(int num);

}
